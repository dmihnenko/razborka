/**
 * Активность компании (СТО/разборка) «по умолчанию».
 *
 * Компания считается активной, пока её явно не приостановили (is_active === false).
 * Это нужно потому, что у части рабочих компаний флаг в БД не выставлен
 * (null/undefined) — раньше такие висели «Неактивно», хотя ими пользуются.
 * Явная деактивация (is_active === false) по-прежнему уважается.
 */
export const isCompanyActive = (company: { is_active?: boolean | null }): boolean =>
  company.is_active !== false
