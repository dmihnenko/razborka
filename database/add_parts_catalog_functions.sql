-- Функция для увеличения счетчика просмотров
CREATE OR REPLACE FUNCTION increment_part_views(part_id UUID)
RETURNS void AS $$
BEGIN
  UPDATE parts_catalog
  SET views_count = views_count + 1
  WHERE id = part_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

