  c.phone,
  COUNT(v.id) as vehicles_count,
  STRING_AGG(v.brand || ' ' || v.model || ' ' || v.year::text, ', ') as vehicles
FROM customers c
LEFT JOIN vehicles v ON v.customer_id = c.id
WHERE c.sto_company_id = 'e0e2202a-e4c2-4505-8b4c-07037cb64281'
